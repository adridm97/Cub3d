#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t destsize)
{
	unsigned long	i;

	i = 0;
	if (!destsize)
		return (ft_strlen(src));
	while (i < destsize -1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}
